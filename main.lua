local function toDecimal (input)
	local e = {}
	print(input:gmatch("."))
	for v in input:gmatch(".") do
		for j,k in pairs (letters_dec) do
			if j == v then
				e[#e+1] = k
			end
		end
	end
	
	return e, table.concat(e, " ")
end

local function DecimaltoString (data)
	local e = {}
	for i,v in ipairs (data) do
		for j,k in pairs (letters_dec) do
			if k == v then
				e[#e+1] = j
			end
		end
	end
	print(data, e)
	return table.concat(e)
end
